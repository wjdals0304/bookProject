package book.domain;

import java.sql.Date;
import java.util.List;

public class PurchasedInfo {

	private String purchasedInfoId;
	private int count;
	private String orderId;
	
	private int totalPrice; 
	
	private Book book;
	
	public PurchasedInfo() {
		super();
	}
	
	public int getTotalPrice(){
		return totalPrice;
	}
	
	public void setTotalPrice(int totalPrice){
		this.totalPrice=totalPrice;
	}
	

	public String getPurchasedInfoId() {
		return purchasedInfoId;
	}

	public void setPurchasedInfoId(String purchasedInfoId) {
		this.purchasedInfoId = purchasedInfoId;
	}

	public int getCount() {
		return count;
	}

	public void setCount(int count) {
		this.count = count;
	}

	public String getOrderId() {
		return orderId;
	}

	public void setOrderId(String orderId) {
		this.orderId = orderId;
	}

	public Book getBook() {
		return book;
	}

	public void setBook(Book book) {
		this.book = book;
	}

	@Override
	public String toString() {
		return "PurchasedInfo [purchasedInfoId=" + purchasedInfoId + ", count=" + count + ", orderId=" + orderId
				+ ", book=" + book + "]";
	}

}
